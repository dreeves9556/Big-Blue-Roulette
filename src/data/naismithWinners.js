// Calculate the 75th percentile stats across all winners (the "Target" baseline)
// 4 attributes: Scoring (pts), Rebounding (trb), Playmaking (ast), Defense (stl+blk)
// Using 75th percentile means you need to be better than 3/4 of Naismith winners to hit 100%
export function calculatePerfectPlayer() {
  const perfect = {};
  
  // Helper to calculate percentile (0-1 = 0%-100%)
  const percentile = (arr, p) => {
    const valid = arr.filter(v => v !== null && v !== undefined && !isNaN(v)).sort((a, b) => a - b);
    if (valid.length === 0) return 0;
    const index = Math.ceil(valid.length * p) - 1;
    return valid[Math.max(0, index)];
  };
  
  // Scoring - 75th percentile points per game (harder target)
  perfect.scoring = percentile(naismithWinners.map(w => w.pts), 0.75);
  
  // Rebounding - 75th percentile rebounds per game
  perfect.rebounding = percentile(naismithWinners.map(w => w.trb), 0.75);
  
  // Playmaking - 75th percentile assists per game
  perfect.playmaking = percentile(naismithWinners.map(w => w.ast), 0.75);
  
  // Defense - 75th percentile combined steals + blocks per game
  // Only include players with BOTH steals and blocks recorded
  const defensePlayers = naismithWinners.filter(w => w.stl !== null && w.blk !== null);
  perfect.defense = percentile(defensePlayers.map(w => w.stl + w.blk), 0.75);
  
  // Round to tenths for cleaner display
  Object.keys(perfect).forEach(key => {
    perfect[key] = Math.round(perfect[key] * 10) / 10;
  });
  
  return perfect;
}

// The "Target Player" - average stats from all Naismith winners (attainable goal)
export const perfectPlayer = calculatePerfectPlayer();

// Get a winner by season
export function getWinnerBySeason(season) {
  return naismithWinners.find(w => w.season === season);
}

// Get random season
export function getRandomSeason() {
  return allNaismithSeasons[Math.floor(Math.random() * allNaismithSeasons.length)];
}
